public class ByteExample {

	public static void main(String[] args) {
		
		byte var1 = -128;
		byte var2 = 0;
		byte var3 = 127;
		// byte var2 = 200; byte범위에 200이 포함되지않기때문에 컴파일오류
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		
	}

}
